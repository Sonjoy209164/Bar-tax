def load_chunks() -> dict[str, dict]:
    chunks: dict[str, dict] = {}
    with CHUNKS_PATH.open(encoding="utf-8") as handle:
        for line in handle:
            if not line.strip():
                continue
            chunk = json.loads(line)
            chunks[chunk["chunk_id"]] = chunk
    return chunks


def assert_temporal_policy(item: dict) -> None:
    expected_tax_year = item.get("expected_tax_year")
    if item.get("should_abstain") or not expected_tax_year:
        return
    query_tax_year = preprocess_query(item["question_text"]).tax_year
    if query_tax_year != expected_tax_year:
        raise ValueError(
            f"{item['question_id']} hides or mismatches tax year: "
            f"query={query_tax_year!r}, expected={expected_tax_year!r}"
        )


def build_rows(chunks: dict[str, dict]) -> list[AnnotatedQuestion]:
    source_rows = [*PILOT14_50_ROWS, *ADDITIONAL_ROWS]
    built: list[AnnotatedQuestion] = []
    seen_question_ids: set[str] = set()
    for item in source_rows:
        question_id = item["question_id"]
        if question_id in seen_question_ids:
            raise ValueError(f"Duplicate question id: {question_id}")
        seen_question_ids.add(question_id)
        assert_temporal_policy(item)

        missing = [chunk_id for chunk_id in item["expected_chunk_ids"] if chunk_id not in chunks]
        if missing:
            raise ValueError(f"{question_id} references missing chunks: {missing}")

        evidence_chunks = [chunks[chunk_id] for chunk_id in item["expected_chunk_ids"]]
        expected_doc_ids = sorted({chunk["doc_id"] for chunk in evidence_chunks})
        expected_sections = sorted({str(chunk["section_id"]) for chunk in evidence_chunks if chunk.get("section_id")})

        built.append(
            AnnotatedQuestion(
                question_id=question_id,
                question_text=item["question_text"],
                question_type=item["question_type"],
                answer_text=item["answer_text"],
                expected_chunk_ids=item["expected_chunk_ids"],
                expected_doc_ids=expected_doc_ids,
                expected_sections=expected_sections,
                expected_tax_year=item["expected_tax_year"],
                preferred_authority_level=item["preferred_authority_level"],
                should_abstain=item["should_abstain"],
                answer_language=item["answer_language"],
                notes=item["notes"],
            )
        )
    if len(built) != 100:
        raise ValueError(f"Expected 100 rows, got {len(built)}")
    if sum(1 for row_item in built if row_item.should_abstain) < 20:
        raise ValueError("Pilot14-100 should include at least 20 abstention rows")
    return built


def write_jsonl(rows: list[AnnotatedQuestion], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for row_item in rows:
            handle.write(json.dumps(row_item.model_dump(), ensure_ascii=False) + "\n")


def write_report(rows: list[AnnotatedQuestion]) -> None:
    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    type_counts: dict[str, int] = {}
    doc_counts: dict[str, int] = {}
    year_counts: dict[str, int] = {}
    abstention_count = 0
    answerable_with_hidden_year = 0
    for row_item in rows:
        type_counts[row_item.question_type] = type_counts.get(row_item.question_type, 0) + 1
        if row_item.should_abstain:
            abstention_count += 1
        for doc_id in row_item.expected_doc_ids:
            doc_counts[doc_id] = doc_counts.get(doc_id, 0) + 1
        if row_item.expected_tax_year:
            year_counts[row_item.expected_tax_year] = year_counts.get(row_item.expected_tax_year, 0) + 1
            if not row_item.should_abstain and preprocess_query(row_item.question_text).tax_year != row_item.expected_tax_year:
                answerable_with_hidden_year += 1

    report_lines = [
        "# Pilot14 Shortlist 100 Report",
        "",
        "Generated on: 2026-05-05",
        "",
        "## Outputs",
        "",
        f"- Annotation shortlist: `{ANNOTATION_SHORTLIST_PATH.relative_to(PROJECT_ROOT)}`",
        f"- Annotator working file: `{ANNOTATOR_WORKING_PATH.relative_to(PROJECT_ROOT)}`",
        f"- Executable dataset: `{DATASET_PATH.relative_to(PROJECT_ROOT)}`",
        "",
        "## Counts",
        "",
        f"- Total rows: {len(rows)}",
        f"- Answerable rows: {len(rows) - abstention_count}",
        f"- Abstention rows: {abstention_count}",
        f"- Answerable hidden-year rows: {answerable_with_hidden_year}",
        "",
        "## Question Type Counts",
        "",
    ]
    for question_type, count in sorted(type_counts.items()):
        report_lines.append(f"- `{question_type}`: {count}")
    report_lines.extend(["", "## Evidence Doc Counts", ""])
    for doc_id, count in sorted(doc_counts.items()):
        report_lines.append(f"- `{doc_id}`: {count}")
    report_lines.extend(["", "## Expected Tax-Year Counts", ""])
    for tax_year, count in sorted(year_counts.items()):
        report_lines.append(f"- `{tax_year}`: {count}")
    report_lines.extend(
        [
            "",
            "## Caveat",
            "",
            "This is an expanded pilot seed, not a final published gold benchmark. Every answer should still be checked against the source PDF before release.",
        ]
    )
    REPORT_PATH.write_text("\n".join(report_lines) + "\n", encoding="utf-8")


def main() -> None:
    chunks = load_chunks()
    rows = build_rows(chunks)
    write_jsonl(rows, ANNOTATION_SHORTLIST_PATH)
    write_jsonl(rows, ANNOTATOR_WORKING_PATH)
    write_jsonl(rows, DATASET_PATH)
    write_report(rows)
    print(f"Wrote {len(rows)} rows")
    print(ANNOTATION_SHORTLIST_PATH.relative_to(PROJECT_ROOT))
    print(ANNOTATOR_WORKING_PATH.relative_to(PROJECT_ROOT))
    print(DATASET_PATH.relative_to(PROJECT_ROOT))
    print(REPORT_PATH.relative_to(PROJECT_ROOT))


if __name__ == "__main__":
    main()
