def assert_temporal_policy(item: dict) -> None:
    expected_tax_year = item.get("expected_tax_year")
    if item.get("should_abstain") or not expected_tax_year:
        return
    query_tax_year = preprocess_query(item["question_text"]).tax_year
    if query_tax_year != expected_tax_year:
        raise ValueError(
            f"{item['question_id']} hides or mismatches tax year: "
            f"query={query_tax_year!r}, expected={expected_tax_year!r}"
        )


def build_rows(chunks: dict[str, dict]) -> list[AnnotatedQuestion]:
    base_rows = build_100_rows(chunks)
    built = list(base_rows)
    seen_question_ids = {row_item.question_id for row_item in base_rows}

    for item in ADDITIONAL_ROWS:
        question_id = item["question_id"]
        if question_id in seen_question_ids:
            raise ValueError(f"Duplicate question id: {question_id}")
        seen_question_ids.add(question_id)
        assert_temporal_policy(item)

        missing = [chunk_id for chunk_id in item["expected_chunk_ids"] if chunk_id not in chunks]
        if missing:
            raise ValueError(f"{question_id} references missing chunks: {missing}")

        evidence_chunks = [chunks[chunk_id] for chunk_id in item["expected_chunk_ids"]]
        expected_doc_ids = sorted({chunk["doc_id"] for chunk in evidence_chunks})
        expected_sections = sorted({str(chunk["section_id"]) for chunk in evidence_chunks if chunk.get("section_id")})

        built.append(
            AnnotatedQuestion(
                question_id=question_id,
                question_text=item["question_text"],
                question_type=item["question_type"],
                answer_text=item["answer_text"],
                expected_chunk_ids=item["expected_chunk_ids"],
                expected_doc_ids=expected_doc_ids,
                expected_sections=expected_sections,
                expected_tax_year=item["expected_tax_year"],
                preferred_authority_level=item["preferred_authority_level"],
                should_abstain=item["should_abstain"],
                answer_language=item["answer_language"],
                notes=item["notes"],
            )
        )

    if len(built) != 150:
        raise ValueError(f"Expected 150 rows, got {len(built)}")
    if sum(1 for row_item in built if row_item.should_abstain) < 30:
        raise ValueError("Pilot14-150 should include at least 30 abstention rows")
    return built


def write_jsonl(rows: list[AnnotatedQuestion], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for row_item in rows:
            handle.write(json.dumps(row_item.model_dump(), ensure_ascii=False) + "\n")


